EDGE_TTS_VOICE = "tr-TR-EmelNeural"   # change to "tr-TR-AhmetNeural" for male voice


async def _edge_tts_to_mp3(text: str, voice: str) -> bytes:
    """Use edge-tts to generate MP3 bytes for the given text and voice."""
    try:
        import edge_tts
    except ImportError:
        print("ERROR: edge-tts not installed.  Run: pip install edge-tts")
        sys.exit(1)

    communicate = edge_tts.Communicate(text, voice)
    mp3_chunks = []
    async for chunk in communicate.stream():
        if chunk["type"] == "audio":
            mp3_chunks.append(chunk["data"])

    if not mp3_chunks:
        print(f"ERROR: edge-tts returned no audio for '{text}'")
        sys.exit(1)

    return b"".join(mp3_chunks)


def generate_mp3(text: str, voice: str) -> bytes:
    """Synchronous wrapper around the async edge-tts call."""
    # On Windows, ProactorEventLoop (default in Python 3.8+) can conflict
    # with edge-tts async networking — force SelectorEventLoop.
    if sys.platform == "win32":
        asyncio.set_event_loop_policy(asyncio.WindowsSelectorEventLoopPolicy())
    return asyncio.run(_edge_tts_to_mp3(text, voice))


